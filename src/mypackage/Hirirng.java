    private void btnNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnNextActionPerformed
        // TODO add your handling code here:
        s5 =txtname.getText();
        
        try{
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:3306/hr_mgt_system", "root", "");
            st = con.createStatement();
            query = ("SELECT * FROM hiringmgt");
            
            rset = st.executeQuery(query);
            
            while(true){
                if(rset.next()){
                    if(rset.getString("CandidateName").equals(s5)){
                        if(rset.next()){
                            txtid.setText(rset.getString("JobID"));
                            txtjobtitle.setText(rset.getString("JobTitle"));
                            txtjobreq.setText(rset.getString("JobRequirements"));
                            txtmanager.setText(rset.getString("HiringManager"));
                            txtname.setText(rset.getString("CandidateName"));
                            String interviewDate = rset.getString("InterviewDate");
                            ((JTextField) iDate.getDateEditor().getUiComponent()).setText(interviewDate);
                            jComboBox1.setSelectedItem(rset.getString("HiringDecision"));
                            
                            break;
                            
                        }
                        else{
                            JOptionPane.showMessageDialog(null,"No more record found" );
                            break;
                        }
                        }
                    }
                       
                }
            rset.close();
            st.close();
            con.close();
            
            }
        catch(ClassNotFoundException | SQLException | HeadlessException ex){
            System.out.println(ex);
        }
    }//GEN-LAST:event_btnNextActionPerformed

